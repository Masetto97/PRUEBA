import socket, pickle
s = socket.socket()
s.connect(("localhost", 50001))
filetosend = open("./Hola.txt", "rb")
aux = filetosend.read(1024)
data = pickle.dumps(aux)
while data:
    print("Sending...")
    s.send(data)
    data = filetosend.read(1024)
filetosend.close()
s.send('fin')
print("Done Sending.")
print(s.recv(1024))
s.shutdown(2)
s.close()
